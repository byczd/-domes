func codeMethodStr2(str:String) -> (String) {
    var returnStr = String()
    for  c in str {
        let keyValue:[String:String] = ["1":"_1",
                                        "2":"_2",
                                        "3":"_3",
                                        "4":"_4",
                                        "5":"_5",
                                        "6":"_6",
                                        "7":"_7",
                                        "8":"_8",
                                        "9":"_9",
                                        "0":"_0",
                                        
                                        ")": "paren_right",
                                        ":": "colon",
                                        "%": "percent",
                                        "(": "paren_left",
                                        "?": "question_mark",
                                        
                                        "$": "`$`",
                                        "{": "curly_left",
                                        "|": "bar",
                                        "]": "bracket_right",
                                        "/": "forward_slash",
                                        
                                        "-": "dash",
                                        "\'": "apostrophe",
                                        "[": "bracket_left",
                                        "&": "ampersand",
                                        ",": "comma",
                                        
                                        "*": "asterisk",
                                        "=": "equals",
                                        ";": "semicolon",
                                        "\"": "quotation",
                                        "": "space",
                                        
                                        "!": "exclamation",
                                        ".": "point",
                                        "\\": "back_slash",
                                        ">": "greater_than",
                                        "+": "plus",
                                        
                                        "_": "underscore",
                                        "<": "less_than",
                                        "}": "curly_right",
                                        "^": "caret",
                                        "#": "pound",
                                        "@": "at"
                                        ]
        guard let value = keyValue["\(c)"] else{
            returnStr = returnStr + ".\(c)"
            continue
        }
        returnStr = returnStr + ".\(value)"
    }
    return returnStr
}

